package com.fiap.heranca;

public class Animal {
	private String locomover;
	private String alimentar;
	private String latido;
	
	public String getLocomover() {
		return locomover;
	}
	public void setLocomover(String locomover) {
		this.locomover = locomover;	
	}
	public String getAlimentar() {
		return alimentar;
	}
	public void setAlimentar(String alimentar) {
		this.alimentar = alimentar;
	}
	public String getLatido() {
		return latido;
	}
	public void setLatido(String latido) {
		this.latido = latido;
	}
	
}